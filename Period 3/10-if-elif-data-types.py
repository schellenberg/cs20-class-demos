# ask user for age
age = input("How old are you? ")

#convert data type
age = int(age)

# if they are 16, tell them to get a driver's license
if age == 16:
    print("Go get a driver's license!")

# if they are 15, tell them to get a learner's license
elif age == 15:
    print("Go get a learner's license!")

